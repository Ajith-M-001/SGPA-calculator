function calculate() {

    var a = parseInt(document.getElementById("dbms").value);
    var b = parseInt(document.getElementById("java").value);
    var c = parseInt(document.getElementById("web").value);
    var d = parseInt(document.getElementById("se").value);
    var e = parseInt(document.getElementById("ot").value);
    var f = parseInt(document.getElementById("mad").value);
    var g = parseInt(document.getElementById("lab1").value);
    var h = parseInt(document.getElementById("lab2").value);
    var i = parseInt(document.getElementById("lab3").value);

    var aa = 3 * marks(a);
    var ab = 3 * marks(b);
    var ac = 4 * marks(c);
    var ad = 4 * marks(d);
    var ae = 3 * marks(e);
    var af = 3 * marks(f);
    var ag = 2 * marks(g);
    var ah = 2 * marks(h);
    var ai = 2 * marks(i);


    var z1 = ((aa + ab + ac + ad + ae + af + ag + ah +ai) / 26);


    document.getElementById("res").value = parseFloat(z1.toFixed(2));
}



